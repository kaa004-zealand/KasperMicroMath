using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace KasperMicroMath
{
    public class KasperMicroMath
    {
        /// <summary>
        /// Adds together 2 ints and returns the result
        /// </summary>
        /// <param name="a">The first int</param>
        /// <param name="b">The second int</param>
        /// <returns>The result of the 2 added together</returns>
        public int Add(int a, int b)
        {
            return a + b;
        }
    }
}
